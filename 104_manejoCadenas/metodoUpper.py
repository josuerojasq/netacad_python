#El método "upper()" hace una copia de la cadena de origen,
# reemplaza todas las letras minúsculas con sus equivalentes en mayúsculas,
# y devuelve la cadena como resultado.
print("Yo sé que no sé nada. Parte 2.".upper())
print("este texto es un Titulo!".upper())

texto = "JoeRojas"
texto = texto.upper()
print(texto)