#El método "lower()" genera una copia de una cadena,
# reemplaza todas las letras mayúsculas con sus equivalentes en minúsculas,
# y devuelve la cadena como resultado. Nuevamente, la cadena original permanece intacta.
print("SiGmA=60".lower())
print("josuerojasq".lower())
print("Carlita & Luciana".lower())
print("".lower())

texto = "JoeRojas"
texto = texto.lower()
print(texto)