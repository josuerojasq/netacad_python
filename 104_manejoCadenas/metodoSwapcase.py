#El método "swapcase()" crea una nueva cadena intercambiando
# todas las letras por mayúsculas o minúsculas dentro de la cadena original:
# los caracteres en mayúscula se convierten en minúsculas y viceversa.
print("Yo sé que no sé nada.".swapcase())
print("Mi nombre es Joel Rojas".swapcase())
print("todo es culpa de la UEFA".swapcase())