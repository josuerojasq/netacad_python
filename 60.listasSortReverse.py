#Todas las listas tienen un método denominado "sort()", que las ordena lo más rápido posible
miLista = [8, 10, 6, 2, 4]
miLista.sort() 
print(miLista)

lst = [5, 3, 1, 2, 4]
print(lst)

lst.sort ()
print(lst) # salida: [1, 2, 3, 4, 5]

#También hay un método de lista llamado reverse(), que puedes usar para invertir la lista.
lst = [5, 3, 1, 2, 4]
print(lst)
    
lst.reverse()
print (lst) # salida: [4, 2, 1, 3, 5]

