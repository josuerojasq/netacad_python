#Python ofrece dos operadores muy poderosos, capaces de revisar la lista
#para verificar si un valor específico está almacenado dentro de la lista o no.
#"in" verifica si un elemento dado está actualmente almacenado en algún lugar dentro de la lista.
#"not in" comprueba si un elemento dado está ausente en una lista.
miLista = [0, 3, 12, 8, 2]

print(5 in miLista)
print(5 not in miLista)
print(12 in miLista)